package edu.tongji.cc.Model;

public class CMStatistics {

    private String classification_id;

    private int month;

    private int day;

    private int has_saled;

    public String getClassification_id() {
        return classification_id;
    }

    public void setClassification_id(String classification_id) {
        this.classification_id = classification_id;
    }

    public int getMonth() {
        return month;
    }

    public void setMonth(int month) {
        this.month = month;
    }

    public int getDay() {
        return day;
    }

    public void setDay(int day) {
        this.day = day;
    }

    public int getHas_saled() {
        return has_saled;
    }

    public void setHas_saled(int has_saled) {
        this.has_saled = has_saled;
    }
}
